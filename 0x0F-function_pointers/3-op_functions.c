#include <stdio.h>
#include "3-calc.h"

/**
 *
 * op_add - add numbers
 *
 * @a: the first number
 *
 * @b: the second number
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *
 * op_sub - sub numbers
 *
 * @a: the first number
 *
 * @b: the second number
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *
 * op_mul - mul numbers
 *
 * @a: the first number
 *
 * @b: the second number
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *
 * op_div - div numbers
 *
 * @a: the first number
 *
 * @b: the second number
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *
 * op_mod - mod numbers
 *
 * @a: the first number
 *
 * @b: the second number
*/

int op_mod(int a, int b)
{
	return (a % b);
}
